export function langOf(req) {
  const lang = req.headers['x-lang']
  return ['pt', 'es', 'en'].includes(lang) ? lang : 'pt'
}

export function t(req, key) {
  const lang = langOf(req)
  return messages[lang][key] ?? messages.pt[key]
}

export function entityName(req, key) {
  const lang = langOf(req)
  return (messages[lang].entity && messages[lang].entity[key]) || messages.pt.entity[key]
}

export function notFound(req, entityKey) {
  const lang = langOf(req)
  return messages[lang].notFound(entityName(req, entityKey))
}
